class Unit {
	constructor(name, unitClass, level, experience, stats, inGameStats, skills, inventory) {
		this.name = name;
		this.unitClass = unitClass;
		this.level = level;
		this.experience = experience;
		this.stats = stats;
		this.inGameStats = inGameStats;
		this.skills = skills;
		this.inventory = inventory;
	}
}